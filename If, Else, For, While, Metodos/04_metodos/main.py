mensagem = '     eu adoro ler a biblia'

print(mensagem.lower()) #imprimira tudo em letra minuscula
print(mensagem.upper()) #imprimira tudo em letra maiuscula
print(mensagem.capitalize()) #imprimira a primeira letra maiuscula
print(mensagem.find('r')) #nos mostrara onde esta a posição de index da letra 'r'
print(mensagem.replace('adoro', 'amo'))# trocara a palavra selecionado por uma nova de sua escolha
print(mensagem.strip()) #removerá os espaços do inicio



