cidade.remove('Salvador') # Removerá Salvador na lista 

cidade.insert( 1, 'Goiania' )# Irá inserir Goiania no index 1

cidade.pop(0) # Sao Paulo não estara na lista mais

cidade.sort() # organizara os items em ordem alfabetica

