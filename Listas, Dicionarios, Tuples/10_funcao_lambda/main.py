# função (pequena) sem nome
# criada para ser adicionada dentro de outra função

# função comum
def somar(x):
    return x + 10

print(somar(2))

# mesma função acima, com LAMBDA
somar10 = lambda x: x + 10
print(somar10(2))

# A função Lambda precisa de argumento e expressão, acima o x é o argumento e X+10 é a expressão
       


